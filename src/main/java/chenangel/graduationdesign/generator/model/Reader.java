package chenangel.graduationdesign.generator.model;

public class Reader {
    private Integer id;
    private String readerid;
    private String password;
    private String readername;
    private String readerclass;
    private String borrowhistory;
    private String sex;
    private String birthday;
    private String identification;
    private String tel;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getReaderid() {
        return readerid;
    }

    public void setReaderid(String readerid) {
        this.readerid = readerid;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public String getReadername() {
        return readername;
    }

    public void setReadername(String readername) {
        this.readername = readername;
    }

    public String getReaderclass() {
        return readerclass;
    }

    public void setReaderclass(String readerclass) {
        this.readerclass = readerclass;
    }

    public String getBorrowhistory() {
        return borrowhistory;
    }

    public void setBorrowhistory(String borrowhistory) {
        this.borrowhistory = borrowhistory;
    }

    public String getSex() {
        return sex;
    }

    public void setSex(String sex) {
        this.sex = sex;
    }

    public String getBirthday() {
        return birthday;
    }

    public void setBirthday(String birthday) {
        this.birthday = birthday;
    }

    public String getIdentification() {
        return identification;
    }

    public void setIdentification(String identification) {
        this.identification = identification;
    }

    public String getTel() {
        return tel;
    }

    public void setTel(String tel) {
        this.tel = tel;
    }
}
